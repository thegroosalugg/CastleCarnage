def replay(player)
  puts SEPARATOR
  play_again
  user_choice = ""
  until user_choice == "n"
    user_choice = gets.chomp.downcase
    case user_choice
    when "y" then play_game
    when "n" then break
    else
      error_message
      puts SEPARATOR
      player[:hp].positive? ? win_art : lose_art
